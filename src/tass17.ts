function computeLongitude(t: number) {
	const longitude = new Float64Array(7)

	for (let i = 0; i < 7; i++) {
		const { terms } = BODIES[i].series[1][0]
		for (const [a, b, c] of terms) longitude[i] += a * Math.sin(b + c * t)
	}

	return longitude
}

function compute(t: number, longitude: Float64Array, index: number) {
	const body = BODIES[index]
	const elem = new Float64Array(body.s0)

	for (const tmt of body.series[0]) {
		let arg = 0
		for (let i = 0; i < 7; i++) arg += tmt.i[i] * longitude[i]
		for (const [a, b, c] of tmt.terms) elem[0] += a * Math.cos(arg + b + c * t)
	}

	elem[0] = body.aam * (1 + elem[0])

	let k = 0

	// first multiterm already calculated: lon[body]
	if (index !== 7) {
		k++
		elem[1] += longitude[index]
	}

	for (; k < body.series[1].length; k++) {
		const tmt = body.series[1][k]
		let arg = 0
		for (let i = 0; i < 7; i++) arg += tmt.i[i] * longitude[i]
		for (const [a, b, c] of tmt.terms) elem[1] += a * Math.sin(arg + b + c * t)
	}

	elem[1] += body.aam * t

	for (const tmt of body.series[2]) {
		let arg = 0
		for (let i = 0; i < 7; i++) arg += tmt.i[i] * longitude[i]
		for (const [a, b, c] of tmt.terms) {
			const x = b + c * t + arg
			elem[2] += a * Math.cos(x)
			elem[3] += a * Math.sin(x)
		}
	}

	for (const tmt of body.series[3]) {
		let arg = 0
		for (let i = 0; i < 7; i++) arg += tmt.i[i] * longitude[i]
		for (const [a, b, c] of tmt.terms) {
			const x = b + c * t + arg
			elem[4] += a * Math.cos(x)
			elem[5] += a * Math.sin(x)
		}
	}

	return elem
}

