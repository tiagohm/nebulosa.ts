// Meeus, Astron. Algorithms 2nd ed (1998). Chap 37. Equ 37.1
// Calculate Pluto heliocentric ICRF coordinates for given julian day.
// This function is accurate to within 0.07" in longitude, 0.02" in latitude
// and 0.000006 AU in radius.
// Note: This function is not valid outside the period of 1885-2099.
export function pluto(time: Time) {
	time = tt(time)
	// Julian centuries since J2000
	const t = (time.day - 2451545 + time.fraction) / DAYSPERJC

	// Calculate mean longitudes for jupiter, saturn and pluto
	const J = 34.35 * DEG2RAD + 3034.9057 * DEG2RAD * t
	const S = 50.08 * DEG2RAD + 1222.1138 * DEG2RAD * t
	const P = 238.96 * DEG2RAD + 144.96 * DEG2RAD * t

	let sLon = 0
	let sLat = 0
	let sRad = 0

	// Calculate periodic terms in table 37.A
	for (let i = 0; i < COEFFS; i++) {
		const a = ARGUMENT[i][0] * J + ARGUMENT[i][1] * S + ARGUMENT[i][2] * P
		const sina = Math.sin(a)
		const cosa = Math.cos(a)

		sLon += LONGITUDE[i][0] * sina + LONGITUDE[i][1] * cosa
		sLat += LATITUDE[i][0] * sina + LATITUDE[i][1] * cosa
		sRad += RADIUS[i][0] * sina + RADIUS[i][1] * cosa
	}

	// J2000 heliocentric coordinates
	const L = DEG2RAD * (238.958116 + 144.96 * t + sLon)
	const B = DEG2RAD * (-3.908239 + sLat)
	const R = 40.7241346 + sRad

	const sl = Math.sin(L)
	const cl = Math.cos(L)
	const sb = Math.sin(B)
	const cb = Math.cos(B)

	// Transform from J2000 ecliptic to ICRF
	const x = R * cl * cb
	const y = R * (sl * cb * COS_OBL_J2000 - sb * SIN_OBL_J2000)
	const z = R * (sl * cb * SIN_OBL_J2000 + sb * COS_OBL_J2000)

	// const p = eraS2p(L, B, R)
	// return matTransposeMulVec(ECLIPTIC_J2000_MATRIX, p, p)

	return [x, y, z] as const
}
